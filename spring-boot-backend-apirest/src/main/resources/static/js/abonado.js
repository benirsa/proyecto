class Abonado{
    constructor(id, dni, nombre, apellido1, apellido2, fechaNacimiento,
        telefono, direccion){
            this.id = id;
            this.dni = dni;
            this.nombre = nombre;
            this.apellido1 = apellido1;
            this.apellido2 = apellido2;
            this.fechaNacimiento = fechaNacimiento;
            this.telefono = telefono;
            this.direccion = direccion;
    }
}